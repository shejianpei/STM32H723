void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	rt_interrupt_enter();
//	rt_enter_critical();
  if(GPIO_Pin == SW2_Pin)
  {
		delay_ms(10);
    if(0 == HAL_GPIO_ReadPin(GPIOE,SW2_Pin)){
			rt_kprintf("SW2 interrupt\r\n");
			magnetism_flag = magnetism_flag + 1;
			if(magnetism_flag > 1){
				magnetism_flag = 0;
			}
		}
//		 rt_sem_release(magnetism_sem);  
  }else if(GPIO_Pin == SW3_Pin){
		delay_ms(10);
		if(0 == HAL_GPIO_ReadPin(GPIOE,SW3_Pin)){
			rt_kprintf("SW3 interrupt\r\n");
			Temp_flag = Temp_flag + 1;
			if(Temp_flag > 1){
				Temp_flag = 0;
			}
		}
	}else if(GPIO_Pin == SW4_Pin){
		delay_ms(10);
		if(0 == HAL_GPIO_ReadPin(GPIOE,SW4_Pin)){
			rt_kprintf("SW4 interrupt\r\n");
			motor_flag = motor_flag + 1;
			if(motor_flag > 1){
				motor_flag = 0;
			}
		}
	
	}else{
		rt_kprintf("SW5 interrupt\r\n");
		magnetism_flag = 0;
		Temp_flag = 0;
		motor_flag = 0;
	}
//	  if(GPIO_Pin == SW3_Pin)
//  {
//			rt_kprintf("SW3 interrupt\r\n");
//  }else if(GPIO_Pin == SW4_Pin){
//		rt_kprintf("SW4 interrupt\r\n");
//	}else if(GPIO_Pin == SW5_Pin){
//		rt_kprintf("SW5 interrupt\r\n");
//	}else{
//		
//		rt_kprintf("SW2 interrupt\r\n");
//		
//	}
//	rt_exit_critical();
	rt_interrupt_leave();
}
